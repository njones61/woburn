        by Jonathan Harr and the Hollywood movie by the same name. This web site was originally developed as a resource for students in <a href="http://ce547.groups.et.byu.net/syllabus/">CE En 647 - Ground Water Modeling</a> taught by <a href="http://www.emrl.byu.edu/njones/">Dr. Norm Jones</a> at <a href="http://www.byu.edu">Brigham Young University</a>.  However, anyone is welcome
